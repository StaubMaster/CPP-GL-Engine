Image Texture::Generate::Gradiant2D()
{
	Image img(16, 16);

	ColorU4 col;
	for (unsigned int y = 0; y < 16; y++)
	{
		col.R = 0xFF * ((float)y) / 16;
		for (unsigned int x = 0; x < 16; x++)
		{
			col.B = 0xFF * ((float)x) / 16;
			img.Pixel(x, y) = col;
		}
	}

	return img;
}



